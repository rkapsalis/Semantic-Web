    public static void printIterator(final Iterator<?> i, final String header) {
        System.out.println(header);
        for (int c = 0; c < header.length(); c++) {
            System.out.print("=");
        }
        System.out.println();

        if (i.hasNext()) {
            while (i.hasNext()) {
                System.out.println(i.next());
            }
        } else {
            System.out.println("<EMPTY>");
        }

        System.out.println();
    }

